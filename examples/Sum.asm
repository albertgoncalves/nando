// 1 + 2 + ... + 100, then infinite loop

    @i      // A = i        // i = 0            // 0000 0000 0001 0000
    M=1     // M[i] = 1                         // 1110 1111 1100 1000
    @sum    // A = sum      // sum = 1          // 0000 0000 0001 0001
    M=0     // M[sum] = 0                       // 1110 1010 1000 1000
(LOOP)
    @i      // A = i                            // 0000 0000 0001 0000
    D=M     // D = M[i]                         // 1111 1100 0001 0000
    @100    // A = 100                          // 0000 0000 0110 0100
    D=D-A   // D -= 100                         // 1110 0100 1101 0000
    @END                                        // 0000 0000 0001 0010
    D;JGT   // if 0 < (i - 100) { goto END }    // 1110 0011 0000 0001
    @i                                          // 0000 0000 0001 0000
    D=M     // D = M[i]                         // 1111 1100 0001 0000
    @sum                                        // 0000 0000 0001 0001
    M=D+M   // M[sum] = D + M[sum]              // 1111 0000 1000 1000
    @i                                          // 0000 0000 0001 0000
    M=M+1   // M[i] += 1                        // 1111 1101 1100 1000
    @LOOP                                       // 0000 0000 0000 0100
    0;JMP   // goto LOOP                        // 1110 1010 1000 0111
(END)
    @END                                        // 0000 0000 0001 0010
    0;JMP   // goto END                         // 1110 1010 1000 0111
